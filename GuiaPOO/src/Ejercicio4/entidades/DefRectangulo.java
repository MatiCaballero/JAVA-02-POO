package Ejercicio4.entidades;

public class DefRectangulo {

    private int base;
    private int altura;

    public DefRectangulo(int base, int altura) {
        this.base = base;
        this.altura = altura;
    }

    public DefRectangulo() {
    }

    public int getBase() {
        return base;
    }

    public void setBase(int base) {
        this.base = base;
    }

    public int getAltura() {
        return altura;
    }

    public void setAltura(int altura) {
        this.altura = altura;
    }

}
